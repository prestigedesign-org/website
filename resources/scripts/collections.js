// Function to initialize a slider
function initializeSlider(sliderContainer) {
    const sliderImages = sliderContainer.querySelectorAll('.gallery-image-wrapper');
    let currentSlide = 0;

    function showSlide(index) {
        for (let i = 0; i < sliderImages.length; i++) {
            sliderImages[i].style.display = 'none';
        }
        sliderImages[index].style.display = 'block';
    }

    function nextSlide() {
        currentSlide++;
        if (currentSlide >= sliderImages.length) {
            currentSlide = 0;
        }
        showSlide(currentSlide);
    }

    // Set the interval for automatic slide change (e.g., every 3 seconds)
    let interval = setInterval(nextSlide, 3000);

    // Optionally, you can stop the automatic slider when the user interacts with it
    sliderContainer.addEventListener('mouseenter', () => {
        clearInterval(interval);
    });

    sliderContainer.addEventListener('mouseleave', () => {
        interval = setInterval(nextSlide, 3000);
    });

    // Initially, show the first slide
    showSlide(currentSlide);
}
// Initialize all sliders with the 'slider-up' class
const allSliders = document.querySelectorAll('.slider-up');
allSliders.forEach((slider) => {
    initializeSlider(slider);
});







